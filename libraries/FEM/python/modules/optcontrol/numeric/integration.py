def getTrapezoidalIntegrationPoints( integrationOrder ):

  result = numpy.ones( ( 2, integrationOrder ) )

  if integrationOrder > 1:
    result[0, :] = numpy.linspace( -1.0, 1.0, integrationOrder )
    result[1, :] *= 2.0 / float( integrationOrder - 1 )

    result[1, 0] *= 1.0 / 2.0
    result[1,-1] *= 1.0 / 2.0
  
  else:
    result[0, 0] = 0.0
    result[1, 0] = 2.0

  return result


