class IntegrationPointProvider(object):

  def __init__(self, providerFunction = None ):
    
    if providerFunction == None:
      self.provider = getGaussLegendreIntegrationPoints
    else:    
      self.provider = providerFunction
    
    self.integrationPoints = {}

  def __call__(self, integrationOrder):
    if not integrationOrder in self.integrationPoints:
      self.integrationPoints[integrationOrder] = self.provider(integrationOrder)
      
    return self.integrationPoints[integrationOrder]
  


# integrandFunction has to map x -> f(x)
# integrationDomains is a list of x-coordinate pairs that shall be integrated on
# integrationPoints: The integration points that shall be used on each domain
def integrate1DFunction( integrandFunction, integrationDomains, integrationPoints ) :
  
  integrationPointCoordinates = integrationPoints[0]
  integrationPointWeights = integrationPoints[1]

  integrationOrder = len(integrationPointCoordinates)

  if integrationOrder != len(integrationPointWeights):
    raise ValueError('Integration point and weight arrays have to have the same length!')

  integral = 0.0

  for domain in integrationDomains:
    domainLength = domain[1] - domain[0]    

    for iIntegrationPoint in range( 0, integrationOrder ):
      r = integrationPointCoordinates[iIntegrationPoint]
      w = integrationPointWeights[iIntegrationPoint]

      x = domain[0] + domainLength * ( r + 1.0 ) / 2.0
      fx = integrandFunction( x )
      integral += fx * domainLength * w / 2.0

  return integral

